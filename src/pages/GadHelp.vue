          multiple burdens and various forms of violence against women and
          girls;
        </li>
        <li>involvement in organizations and collective action; and</li>
        <li>control over resources and decision-making processes.</li>
      </ul>
      <p>
        Agricultural and agrarian reform programs and projects may be designed
