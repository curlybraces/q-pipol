        </li>
        <li>increased number of women adopting new technologies or crops;</li>
        <li>increased employment of rural women and men;</li>
        <li>
          improved leadership capacity of women involved in farmers’
          organizations and similar groups;
        </li>
        <li>
          greater representation of women in producers, marketing, and similar
          rural organizations; and
        </li>
        <li>
          improved capacity of rural development agencies to plan, design,
          implement, and monitor programs and projects that address gender
          issues and the concerns of woman farmers.
        </li>
      </ul>
